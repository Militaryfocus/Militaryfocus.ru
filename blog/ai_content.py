class ContentScheduler:
    """–ü–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–π –ø—É–±–ª–∏–∫–∞—Ü–∏–∏ –∫–æ–Ω—Ç–µ–Ω—Ç–∞"""
    
    def __init__(self):
        self.generator = AIContentGenerator()
    
    def create_scheduled_post(self, category_name: str = None) -> bool:
        """–°–æ–∑–¥–∞–Ω–∏–µ –∑–∞–ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ –ø–æ—Å—Ç–∞"""
        try:
            # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç
            post_data = self.generator.generate_human_like_post(category_name)
            
            # –ü–æ–ª—É—á–∞–µ–º –∏–ª–∏ —Å–æ–∑–¥–∞–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏—é
            category = Category.query.filter_by(name=post_data['category']).first()
            if not category:
                category = Category(
                    name=post_data['category'],
                    description=f"–°—Ç–∞—Ç—å–∏ –æ {post_data['category']}"
                )
                db.session.add(category)
                db.session.flush()
            
            # –ü–æ–ª—É—á–∞–µ–º —Å–ª—É—á–∞–π–Ω–æ–≥–æ –∞–≤—Ç–æ—Ä–∞ (–∞–¥–º–∏–Ω–∞)
            author = User.query.filter_by(is_admin=True).first()
            if not author:
                return False
            
            # –°–æ–∑–¥–∞–µ–º –ø–æ—Å—Ç
            post = Post(
                title=post_data['title'],
                content=post_data['content'],
                excerpt=post_data['excerpt'],
                category_id=category.id,
                author_id=author.id,
                is_published=True,
                published_at=datetime.utcnow()
            )
            
            db.session.add(post)
            db.session.flush()
            
            # –î–æ–±–∞–≤–ª—è–µ–º —Ç–µ–≥–∏
            for tag_name in post_data['tags']:
                tag = Tag.query.filter_by(name=tag_name).first()
                if not tag:
                    tag = Tag(name=tag_name)
                    db.session.add(tag)
                    db.session.flush()
                post.tags.append(tag)
            
            db.session.commit()
            
            # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏ —á–µ—Ä–µ–∑ –Ω–µ–∫–æ—Ç–æ—Ä–æ–µ –≤—Ä–µ–º—è
            self.schedule_comments(post.id)
            
            print(f"‚úÖ –°–æ–∑–¥–∞–Ω –ø–æ—Å—Ç: {post.title}")
            return True
            
        except Exception as e:
            db.session.rollback()
            print(f"‚ùå –û—à–∏–±–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è –ø–æ—Å—Ç–∞: {e}")
            return False
    
